const CartList = function (
  id,
  image,
  name,
  price,
  description,
  inventory,
  rating,
  type,
  quantity
) {
  this.id = id;
  this.image = image;
  this.name = name;
  this.price = price;
  this.description = description;
  this.inventory = inventory;
  this.rating = rating;
  this.type = type;
  this.quantity = quantity;
};
